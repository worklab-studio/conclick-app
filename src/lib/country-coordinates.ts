/**
 * Get coordinates for a country code
 * Adds small random offset to prevent markers from stacking at exact same point
 */
export function getCountryCoordinates(
  countryCode: string | null | undefined,
): { lat: number; lng: number } | null {
  if (!countryCode) return null;

  const coords = countryCoordinates[countryCode.toUpperCase()];
  if (!coords) return null;

  // Add small random offset (Â±2 degrees) to prevent markers from stacking
  const offset = () => (Math.random() - 0.5) * 4;

  return {
    lat: coords.lat + offset(),
    lng: coords.lng + offset(),
  };
}
